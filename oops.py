class device:

    def __init__(self, brand, color):
        self.brand = brand
        self.color = color
    
    def turn_on(self):
        print(f"turning on {self.brand} ")

    def turn_of(self):
        print(f"turning off {self.brand} ")

bike = device('duke','orange')

print(bike.brand)
print(bike.color)


